package com.bitwise.app.common.datastructure.property;

public class JoinConfigProperty {

	private String port_index;
	private String join_type;
	private Integer join_key;
	
	public String getPort_index() {
		return port_index;
	}
	public void setPort_index(String port_index) {
		this.port_index = port_index;
	}
	public String getJoin_type() {
		return join_type;
	}
	public void setJoin_type(String join_type) {
		this.join_type = join_type;
	}
	public Integer getJoin_key() {
		return join_key;
	}
	public void setJoin_key(Integer join_key) {
		this.join_key = join_key;
	}
}
